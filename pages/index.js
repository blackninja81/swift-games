export const getServerSideProps = async () => {
  const { data } = await axios.get(process.env.REACT_API_URL + `/games`, {
    headers: {
      Authorization: " bearer " + process.env.REACT_ACCESS_TOKEN,
    },
  });
  const res = data.data;
  return {
    props: { res, res },
  };
};
